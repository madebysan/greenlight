type TabKey = (typeof TABS)[number]["key"];

type StepResultsProps = {
  documents: DocumentResult[];
  jsonData?: string;
  onJsonDataChange?: (newJsonData: string) => void;
  onStartOver: () => void;
  onDocumentUpdate?: (slug: string, newContent: string) => void;
  onDocumentRewrite?: (slug: string) => Promise<void>;
  storyboardImages: Record<number, SavedImage>;
  onStoryboardImagesChange: (images: Record<number, SavedImage>) => void;
  promptOverrides: Record<number, string>;
  onPromptOverridesChange: (overrides: Record<number, string>) => void;
  posterImages: Record<number, SavedImage>;
  onPosterImagesChange: (images: Record<number, SavedImage>) => void;
  portraits: Record<string, SavedImage>;
  onPortraitsChange: (portraits: Record<string, SavedImage>) => void;
  propImages: Record<string, SavedImage>;
  onPropImagesChange: (images: Record<string, SavedImage>) => void;
  disabledItems: Record<string, boolean>;
  onDisabledItemsChange: (items: Record<string, boolean>) => void;
};

export function StepResults({
  documents,
  jsonData,
  onJsonDataChange,
  onStartOver,
  onDocumentUpdate,
  storyboardImages,
  onStoryboardImagesChange,
  promptOverrides,
  onPromptOverridesChange,
  posterImages,
  onPosterImagesChange,
  portraits,
  onPortraitsChange,
  propImages,
  onPropImagesChange,
  disabledItems,
  onDisabledItemsChange,
}: StepResultsProps) {
  const [activeTab, setActiveTab] = useState<TabKey>("overview");
  const activeTabIndex = TABS.findIndex((tab) => tab.key === activeTab);
  const previousTab = activeTabIndex > 0 ? TABS[activeTabIndex - 1] : undefined;
  const nextTab = activeTabIndex < TABS.length - 1 ? TABS[activeTabIndex + 1] : undefined;

  const navigateToTab = useCallback((tab: TabKey) => {
    setActiveTab(tab);
    window.requestAnimationFrame(() => {
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  }, []);

  const toSectionNavItem = (
    tab: (typeof TABS)[number] | undefined
  ): ReportSectionNavItem | undefined => {
    if (!tab) return undefined;
    return {
      index: tab.index,
      label: tab.label,
      onClick: () => navigateToTab(tab.key),
    };
  };

  // Keyboard shortcuts for tab navigation:
  //   ⌥/Alt + ←/→  → previous / next tab
  //   ⌥/Alt + 1..8 → jump to tab N
  // Modifier required so single arrow keys keep working in textareas/inputs
  // and don't fight inline-edit affordances.
  useEffect(() => {
    function onKey(e: KeyboardEvent) {
      if (!e.altKey) return;
      const target = e.target as HTMLElement | null;
      // Bail if we're inside any editable surface — the user is typing.
      if (target && (target.tagName === "INPUT" || target.tagName === "TEXTAREA" || target.isContentEditable)) {
        return;
      }
      const idx = TABS.findIndex((t) => t.key === activeTab);
      if (e.key === "ArrowRight") {
        e.preventDefault();
        const next = TABS[(idx + 1) % TABS.length];
        navigateToTab(next.key);
        return;
      }
      if (e.key === "ArrowLeft") {
        e.preventDefault();
        const prev = TABS[(idx - 1 + TABS.length) % TABS.length];
        navigateToTab(prev.key);
        return;
      }
      const numKey = Number(e.key);
      if (Number.isInteger(numKey) && numKey >= 1 && numKey <= TABS.length) {
        e.preventDefault();
        navigateToTab(TABS[numKey - 1].key);
      }
    }
    window.addEventListener("keydown", onKey);
    return () => window.removeEventListener("keydown", onKey);
  }, [activeTab, navigateToTab]);

  const getDoc = (slug: string) => documents.find((d) => d.slug === slug);
  const getDocContent = (slug: string): string | null => {
    const doc = getDoc(slug);
    return doc?.status === "done" ? doc.content : null;
  };

  const failedDocs = documents.filter((d) => d.status === "error");

  const overviewContent = getDocContent("overview");
  const moodContent = getDocContent("mood-and-tone");
  const sceneContent = getDocContent("scene-breakdown");
  const storyboardContent = getDocContent("storyboard-prompts");
  const posterContent = getDocContent("poster-concepts");

  if (!jsonData) {
    return (
      <div className="text-center py-12">
        <p className="text-muted-foreground">
          No screenplay data is loaded. Paste the JSON again.
        </p>
        <Button className="mt-4" onClick={onStartOver}>
          Start Over
        </Button>
      </div>
    );
  }

  return (
    <div className="space-y-4">
      {failedDocs.length > 0 && (
        <div className="rounded-lg border border-destructive/50 bg-destructive/5 p-4">
          <p className="text-sm font-medium text-destructive mb-2">
            Some report sections failed:
          </p>
          <ul className="space-y-1">
            {failedDocs.map((doc) => (
              <li key={doc.slug} className="text-sm text-destructive/80">
                {doc.name}: {doc.error}
              </li>
            ))}
          </ul>
        </div>
      )}

      <div className="grid min-h-[600px] gap-8 pt-6 lg:grid-cols-[240px_minmax(0,1fr)] xl:grid-cols-[260px_minmax(0,1fr)]">
        <aside className="lg:sticky lg:top-24 lg:self-start">
          <div className="border-l border-border/80 pl-4">
            <div className="px-2 pb-3 pt-1">
              <div className="font-mono text-[11px] font-semibold uppercase tracking-[0.12em] text-muted-foreground">
                Report
              </div>
              <p className="mt-2 text-[13px] leading-[1.5] text-foreground/58">
                Explore departments.
              </p>
            </div>

            <nav className="space-y-1.5" aria-label="Report sections">
              {TABS.map((tab) => (
                <ReportNavButton
                  key={tab.key}
                  active={tab.key === activeTab}
                  icon={tab.icon}
                  index={tab.index}
                  label={tab.label}
                  role={tab.role}
                  onClick={() => navigateToTab(tab.key)}
                />
              ))}
            </nav>
          </div>
        </aside>

        <section className="min-w-0 overflow-hidden">
          <div className="pb-8">
            {activeTab === "overview" && (
              overviewContent ? (
                <OverviewViewer
                  content={overviewContent}
                  jsonData={jsonData}
                  onContentUpdate={
                    onDocumentUpdate
                      ? (c) => onDocumentUpdate("overview", c)
                      : undefined
                  }
                  posterContent={posterContent}
                  posterImages={posterImages}
                  onNavigateToPosters={() => navigateToTab("posters")}
                />
              ) : (
                <GenerationPending label="Overview" />
              )
            )}

            {activeTab === "mood-and-tone" && (
              moodContent ? (
                <MoodAndToneViewer
                  content={moodContent}
                  jsonData={jsonData}
                  onContentUpdate={
                    onDocumentUpdate
                      ? (c) => onDocumentUpdate("mood-and-tone", c)
                      : undefined
                  }
                />
              ) : (
                <GenerationPending label="Mood & Tone" />
              )
            )}

            {activeTab === "scenes" && (
              sceneContent ? (
                <SceneBreakdownViewer
                  content={sceneContent}
                  onContentChange={
                    onDocumentUpdate
                      ? (c) => onDocumentUpdate("scene-breakdown", c)
                      : undefined
                  }
                  storyboardContent={storyboardContent}
                  storyboardImages={storyboardImages}
                  onStoryboardImagesChange={onStoryboardImagesChange}
                  promptOverrides={promptOverrides}
                  onPromptOverridesChange={onPromptOverridesChange}
                />
              ) : (
                <GenerationPending label="Scenes" />
              )
            )}

            {activeTab === "locations" && (
              <LocationsViewer jsonData={jsonData} />
            )}

            {activeTab === "cast-and-crew" && (
              <CastAndCrewViewer
                jsonData={jsonData}
                onJsonDataChange={onJsonDataChange}
                portraits={portraits}
                onPortraitsChange={onPortraitsChange}
                disabledItems={disabledItems}
                onDisabledItemsChange={onDisabledItemsChange}
              />
            )}

            {activeTab === "production" && (
              <ProductionViewer
                jsonData={jsonData}
                propImages={propImages}
                onPropImagesChange={onPropImagesChange}
              />
            )}

            {activeTab === "identity" && (
              <IdentityViewer
                jsonData={jsonData}
                moodContent={moodContent}
                onMoodContentUpdate={
                  onDocumentUpdate
                    ? (c) => onDocumentUpdate("mood-and-tone", c)
                    : undefined
                }
              />
            )}

            {activeTab === "posters" && (
              <PostersViewer
                posterContent={posterContent}
                posterImages={posterImages}
                onPosterImagesChange={onPosterImagesChange}
              />
            )}

            <ReportSectionNav
              previous={toSectionNavItem(previousTab)}
              next={toSectionNavItem(nextTab)}
            />
          </div>
        </section>
      </div>
    </div>
  );
}

function ReportNavButton({
  active,
  icon: Icon,
  index,
  label,
  role,
  onClick,
}: {
  active: boolean;
  icon: LucideIcon;
  index: string;
  label: string;
  role: string;
  onClick: () => void;
}) {
  return (
    <button
      type="button"
      onClick={onClick}
      className={`group grid w-full grid-cols-[28px_1fr_auto] items-center gap-3 rounded-[8px] border px-2.5 py-2.5 text-left transition-colors ${
        active
          ? "border-foreground/20 bg-white/[0.06] text-foreground"
          : "border-transparent text-muted-foreground hover:border-border hover:bg-white/[0.025] hover:text-foreground"
      }`}
    >
      <span
        className={`flex h-7 w-7 items-center justify-center rounded-[7px] transition-colors ${
          active
            ? "bg-white text-black"
            : "bg-white/[0.04] text-muted-foreground group-hover:text-foreground"
        }`}
      >
        <Icon size={13} />
      </span>
      <span className="min-w-0">
        <span className="block truncate text-[13px] font-semibold">
          {label}
        </span>
        <span className="mt-0.5 block truncate font-mono text-[10px] uppercase tracking-[0.08em] opacity-65">
          {role}
        </span>
      </span>
      <span className="font-mono text-[10px] uppercase tracking-[0.08em] opacity-50">
        {index}
      </span>
    </button>
  );
}

function GenerationPending({ label }: { label: string }) {
  return (
    <div className="report-motion-status flex flex-col items-center justify-center gap-3 py-12 text-center text-sm text-muted-foreground">
      <span className="report-loading-dot h-1.5 w-1.5 rounded-full bg-muted-foreground/70" />
      <span>{label} is still waiting on generation.</span>
    </div>
  );
}
