"use client";

import { useState } from "react";
import { Button } from "@/components/ui/button";
import { Download } from "lucide-react";
import { OverviewViewer } from "@/components/viewers/overview-viewer";
import { MoodAndToneViewer } from "@/components/viewers/mood-and-tone-viewer";
import { SceneBreakdownViewer } from "@/components/viewers/scene-breakdown-viewer";
import { LocationsViewer } from "@/components/viewers/locations-viewer";
import { CastAndCrewViewer } from "@/components/viewers/cast-and-crew-viewer";
import { ProductionViewer } from "@/components/viewers/production-viewer";
import { VisualsViewer } from "@/components/viewers/visuals-viewer";
import type { DocumentResult } from "./wizard-shell";
import type { SavedImage } from "@/lib/reports";
import { downloadBlob } from "@/lib/utils";

// Display-layer tabs. Maps 1:1 to what the user sees, NOT 1:1 to the documents
// generated by Claude. Derived tabs (locations, cast-and-crew) read from jsonData
// directly. Composite tabs (visuals) combine multiple generated docs.
const TABS = [
  { key: "overview", label: "Overview" },
  { key: "mood-and-tone", label: "Mood & Tone" },
  { key: "scenes", label: "Scenes" },
  { key: "locations", label: "Locations" },
  { key: "cast-and-crew", label: "Cast & Crew" },
  { key: "production", label: "Production" },
  { key: "visuals", label: "Visuals" },
] as const;

type TabKey = (typeof TABS)[number]["key"];

type StepResultsProps = {
  documents: DocumentResult[];
  jsonData?: string;
  onStartOver: () => void;
  onDocumentUpdate?: (slug: string, newContent: string) => void;
  onDocumentRewrite?: (slug: string) => Promise<void>;
  storyboardImages: Record<number, SavedImage>;
  onStoryboardImagesChange: (images: Record<number, SavedImage>) => void;
  promptOverrides: Record<number, string>;
  onPromptOverridesChange: (overrides: Record<number, string>) => void;
  posterImages: Record<number, SavedImage>;
  onPosterImagesChange: (images: Record<number, SavedImage>) => void;
  portraits: Record<string, SavedImage>;
  onPortraitsChange: (portraits: Record<string, SavedImage>) => void;
};

export function StepResults({
  documents,
  jsonData,
  onStartOver,
  storyboardImages,
  onStoryboardImagesChange,
  promptOverrides,
  onPromptOverridesChange,
  posterImages,
  onPosterImagesChange,
  portraits,
  onPortraitsChange,
}: StepResultsProps) {
  const [activeTab, setActiveTab] = useState<TabKey>("overview");

  const getDoc = (slug: string) => documents.find((d) => d.slug === slug);
  const getDocContent = (slug: string): string | null => {
    const doc = getDoc(slug);
    return doc?.status === "done" ? doc.content : null;
  };

  const failedDocs = documents.filter((d) => d.status === "error");

  const overviewContent = getDocContent("overview");
  const moodContent = getDocContent("mood-and-tone");
  const sceneContent = getDocContent("scene-breakdown");
  const storyboardContent = getDocContent("storyboard-prompts");
  const posterContent = getDocContent("poster-concepts");

  const activeDocContent: { content: string; filename: string } | null = (() => {
    switch (activeTab) {
      case "overview":
        return overviewContent ? { content: overviewContent, filename: "overview.md" } : null;
      case "mood-and-tone":
        return moodContent ? { content: moodContent, filename: "mood-and-tone.md" } : null;
      case "scenes":
        return sceneContent ? { content: sceneContent, filename: "scenes.md" } : null;
      default:
        return null;
    }
  })();

  const handleDownloadActive = () => {
    if (!activeDocContent) return;
    downloadBlob(activeDocContent.content, activeDocContent.filename, "text/markdown");
  };

  if (!jsonData) {
    return (
      <div className="text-center py-12">
        <p className="text-muted-foreground">
          No screenplay data loaded. Try pasting the JSON again.
        </p>
        <Button className="mt-4" onClick={onStartOver}>
          Start Over
        </Button>
      </div>
    );
  }

  return (
    <div className="space-y-4">
      {failedDocs.length > 0 && (
        <div className="rounded-lg border border-destructive/50 bg-destructive/5 p-4">
          <p className="text-sm font-medium text-destructive mb-2">
            Some documents failed to generate:
          </p>
          <ul className="space-y-1">
            {failedDocs.map((doc) => (
              <li key={doc.slug} className="text-sm text-destructive/80">
                {doc.name}: {doc.error}
              </li>
            ))}
          </ul>
        </div>
      )}

      <div className="min-h-[600px]">
        <div className="flex items-center gap-1 border-b mb-0">
          {TABS.map((tab) => {
            const isActive = tab.key === activeTab;
            return (
              <button
                key={tab.key}
                onClick={() => setActiveTab(tab.key)}
                className={`relative px-4 py-2.5 text-[13px] font-medium transition-colors rounded-t-lg ${
                  isActive
                    ? "text-foreground bg-muted/50"
                    : "text-muted-foreground hover:text-foreground hover:bg-muted/30"
                }`}
              >
                {tab.label}
                {isActive && (
                  <span className="absolute bottom-0 left-0 right-0 h-0.5 bg-primary rounded-full" />
                )}
              </button>
            );
          })}
          {activeDocContent && (
            <button
              onClick={handleDownloadActive}
              className="ml-auto flex items-center gap-1.5 text-xs text-muted-foreground hover:text-foreground hover:bg-muted rounded-md px-2 py-1 transition-colors"
            >
              <Download size={14} />
              Download
            </button>
          )}
        </div>

        <div className="rounded-b-lg border border-t-0 overflow-hidden">
          <div className="overflow-y-auto p-6">
            {activeTab === "overview" && (
              overviewContent ? (
                <OverviewViewer content={overviewContent} jsonData={jsonData} />
              ) : (
                <GenerationPending label="Overview" />
              )
            )}

            {activeTab === "mood-and-tone" && (
              moodContent ? (
                <MoodAndToneViewer content={moodContent} />
              ) : (
                <GenerationPending label="Mood & Tone" />
              )
            )}

            {activeTab === "scenes" && (
              sceneContent ? (
                <SceneBreakdownViewer content={sceneContent} />
              ) : (
                <GenerationPending label="Scenes" />
              )
            )}

            {activeTab === "locations" && (
              <LocationsViewer jsonData={jsonData} />
            )}

            {activeTab === "cast-and-crew" && (
              <CastAndCrewViewer
                jsonData={jsonData}
                portraits={portraits}
                onPortraitsChange={onPortraitsChange}
              />
            )}

            {activeTab === "production" && (
              <ProductionViewer jsonData={jsonData} />
            )}

            {activeTab === "visuals" && (
              <VisualsViewer
                storyboardContent={storyboardContent}
                posterContent={posterContent}
                storyboardImages={storyboardImages}
                onStoryboardImagesChange={onStoryboardImagesChange}
                promptOverrides={promptOverrides}
                onPromptOverridesChange={onPromptOverridesChange}
                posterImages={posterImages}
                onPosterImagesChange={onPosterImagesChange}
              />
            )}
          </div>
        </div>
      </div>
    </div>
  );
}

function GenerationPending({ label }: { label: string }) {
  return (
    <div className="text-sm text-muted-foreground py-12 text-center">
      {label} content hasn&apos;t been generated yet.
    </div>
  );
}
